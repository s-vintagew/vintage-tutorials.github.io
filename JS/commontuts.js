function show_hdem()
{
    
    if(document.getElementById("disp_hdem").style.display=="block")
        {
            document.getElementById("disp_hdem").style.display="none";
        }
    else
        {
            document.getElementById("disp_hdem").style.display="block";
        }
}
function show()
{
    
    if(document.getElementById("disp").style.display=="block")
        {
            document.getElementById("disp").style.display="none";
        }
    else
        {
            document.getElementById("disp").style.display="block";
        }
}